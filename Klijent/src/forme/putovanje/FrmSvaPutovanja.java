    private void popuniTabelu() {
        ModelTabelePutovanja model = new ModelTabelePutovanja();
        try {
            ArrayList<Putovanje> lista = Komunikacija.getInstance().ucitajListuPutovanja();
            //model.setPutnici(lista);
            tblPutovanja.setModel(new ModelTabelePutovanja(lista));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Greska prilikom popunjavanja tabele");
            Logger.getLogger(FrmSvaPutovanja.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void prepareView() {
        ModelTabelePutovanja model = (ModelTabelePutovanja) tblPutovanja.getModel();
        boolean isEmpty = model.getRowCount() == 0;
        btnIzmeni.setEnabled(!isEmpty);
    }
