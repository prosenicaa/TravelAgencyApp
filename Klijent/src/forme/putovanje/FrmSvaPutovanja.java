    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        ModelTabelePutovanja model = (ModelTabelePutovanja) tblPutovanja.getModel();
        int i = tblPutovanja.getSelectedRow();
        
        if(i >= 0) {
            Putovanje p = model.vratiPutovanje(i);
            
            try {
                p = Komunikacija.getInstance().ucitajPutovanje(p);
                
                JOptionPane.showMessageDialog(this, "Sistem je ucitao putovanje");
                
               FrmNovoPutovanje fnp = new FrmNovoPutovanje(this.parent, true);
                fnp.setPutovanje(p);
                fnp.setVisible(true);
                
                prepareView();
                popuniTabelu();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita putovanje");
                Logger.getLogger(FrmSvaPutovanja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita putovanje");
        }
        
        
        
    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void txtPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPretragaActionPerformed
       try {
            Putovanje p = new Putovanje();
            p.setPrevoz(txtPretraga.getText());
            p.setNaziv(txtPretraga.getText());


            
            ArrayList<Putovanje> lista = Komunikacija.getInstance().ucitajListuPutovanja(p);
            ModelTabelePutovanja model = new ModelTabelePutovanja();
            model.setPutovanje(lista);
            tblPutovanja.setModel(model);
            
            //prepareView();
            
            if(model.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putovanja po zadatoj vrednosti");
            }else{
                JOptionPane.showMessageDialog(this, "Sistem je nasao putovanja po zadatoj vrednosti");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putovanja po zadatoj vrednosti");
            Logger.getLogger(FrmSvaPutovanja.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_txtPretragaActionPerformed


