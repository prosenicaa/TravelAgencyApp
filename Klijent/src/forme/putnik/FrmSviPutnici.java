    private void txtPretragaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPretragaActionPerformed
        try {
            Putnik putnik = new Putnik();
            putnik.setIme(txtPretraga.getText());
            putnik.setPrezime(txtPretraga.getText());
            putnik.setBrojPasosa(txtPretraga.getText());
            putnik.setEmail(txtPretraga.getText());
            putnik.setTelefon(txtPretraga.getText());
            
            ArrayList<Putnik> lista = Komunikacija.getInstance().ucitajListuPutnika(putnik);
            ModelTabelePutnik model = new ModelTabelePutnik();
            model.setPutnici(lista);
            tblPutnici.setModel(model);
            
            //prepareView();
            
            if(model.getRowCount() == 0){
                JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putnike po zadatoj vrednosti");
            }else{
                JOptionPane.showMessageDialog(this, "Sistem je nasao putnike po zadatoj vrednosti");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje putnike po zadatoj vrednosti");
            Logger.getLogger(FrmSviPutnici.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_txtPretragaActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        ModelTabelePutnik model = (ModelTabelePutnik) tblPutnici.getModel();
        int i = tblPutnici.getSelectedRow();
        
        if(i >= 0) {
            Putnik p = model.vratiPutnika(i);
            
            try {
                p = Komunikacija.getInstance().ucitajPutnika(p);
                
                JOptionPane.showMessageDialog(this, "Sistem je ucitao putnika");
                FrmNoviPutnik fnp = new FrmNoviPutnik(this.parent, true);
                fnp.setPutnik(p);
                fnp.setVisible(true);
                
                prepareView();
                popuniTabelu();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita putnika");
                Logger.getLogger(FrmSviPutnici.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }else{
            JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita putnika");
        }
    }//GEN-LAST:event_btnIzmeniActionPerformed
    
    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        ModelTabelePutnik model = (ModelTabelePutnik) tblPutnici.getModel();
        int i = tblPutnici.getSelectedRow();
        
        if(i >= 0) {
            Putnik p = model.vratiPutnika(i);
            
            try {
                p = Komunikacija.getInstance().ucitajPutnika(p);
                
                JOptionPane.showMessageDialog(this, "Sistem je ucitao putnika");
                
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da ucita putnika");
                Logger.getLogger(FrmSviPutnici.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
                Komunikacija.getInstance().obrisiPutnika(p);
                model.obrisiPutnika(i);
                prepareView();
                JOptionPane.showMessageDialog(this, "Sistem je uspesno izbrisao putnika");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da izbrise putnika");
                Logger.getLogger(FrmSviPutnici.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else{
            JOptionPane.showMessageDialog(this, "Sistem ne moze da izbrise putnika");
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

