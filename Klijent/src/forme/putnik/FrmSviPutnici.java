    private void popuniTabelu() {
        try {
            ArrayList<Putnik> lista = Komunikacija.getInstance().ucitajListuPutnika();
            tblPutnici.setModel(new ModelTabelePutnik(lista));
        } catch (Exception ex) {
            Logger.getLogger(FrmSviPutnici.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
    }

    private void prepareView() {
        ModelTabelePutnik model = (ModelTabelePutnik) tblPutnici.getModel();
        boolean isEmpty = model.getRowCount() == 0;
        btnIzmeni.setEnabled(!isEmpty);
        btnObrisi.setEnabled(!isEmpty);
    }
